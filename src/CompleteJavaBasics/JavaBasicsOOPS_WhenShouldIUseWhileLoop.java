package CompleteJavaBasics;

public class JavaBasicsOOPS_WhenShouldIUseWhileLoop {
    public static void main(String[] args) {

        // Using while loop print the numbers from 1 to 10
        int i = 1;
        while(i<=10){
            System.out.println("Print the numbers from 1 to 10: " + i);
            i++;
        }

        // Using while loop print the numbers from 10 to 9
        int j = 10;
        while(j>=1){
            System.out.println("Print the numbers from 10 to 1: " + j);
            j--;
        }
    }
}
